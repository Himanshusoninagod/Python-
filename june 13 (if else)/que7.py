# Nested if--
option=int(input("1. monday 2. tuesday 3. wednesday 4. thursday 5. friday 6. saturday 7. sunday: "))

if option==1 :
    print("Monday")
elif option==2 :
    print("Tuesday")
elif option==3 :
    print("Wednesday")
elif option==4 :
    print("Thursday")
elif option==5 :
    print("Friday")
elif option==6 :
    print("Saturday")
elif option==7 :
    print("Sunday")
else :
    print("Invalid")